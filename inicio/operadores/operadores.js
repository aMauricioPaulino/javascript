// Binários
// É um operador que opera em cima de dois operando, ex: (3 + 4).

// Unários
// É é um ou mais operadores que só operam em cima de um operando, ex: (a++).

// Ternários
// É quando um operador opera em cima de 3 parte ou operandos, ex: (x + a: b).

