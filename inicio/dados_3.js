console.log(typeof idade); // Está mostrando que tipo de variável ela é.

// Mais uma exemplo:

let salario = 4578.32; // Isso é chamado de número com ponto flutuante. Ou seja, ele pode flutuar por todo o número dando diferentes valores a ele.
console.log(typeof salario); // Repare que diferente do pseudocódigo aqui no JS não tem diferença entre inteiro e real, todos são da variável (NUMBER).


let estaChovendo = true; // ou false
console.log(typeof estaChovendo); // Lembrando que (TYPEOF) está sendo usado para identificar que tipo de variável é.

